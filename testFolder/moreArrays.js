//every
let ev = [2,4,6,8,10];
let everyVal = ev.every((e) => e%2 === 0);
console.log(everyVal);

//some
let sm = [1,2,4,6,8,10];
let someVal = sm.some((e) => e%2 !== 0);
console.log(someVal);

//find
let fn = [1,2,4,6,8,10];
let ff = fn.find((f) => f%2 === 0);
console.log(ff);

//indexOf
let names = ["sohail", "john", "dave", "steve"];
console.log(names.indexOf("john"));

//lastIndexOf()
let nm = ["sohail", "john", "dave", "steve", "sohail"];
console.log(nm.lastIndexOf("sohail"));

//reverse()
let rev = ["sohail", "john", "dave", "steve"];
console.log(rev.reverse());

//sort()
let sorting = ["sohail", "john", "dave", "steve"];
console.log(sorting.sort());











